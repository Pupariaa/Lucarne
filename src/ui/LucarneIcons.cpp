const uint16_t *iconData(IconId id) {
    uint8_t idx = (uint8_t)id;
    if (idx >= (uint8_t)IconId::Count) return nullptr;
    return ICON_TABLE[idx].data;
}

const char *iconName(IconId id) {
    uint8_t idx = (uint8_t)id;
    if (idx >= (uint8_t)IconId::Count) return "none";
    return ICON_TABLE[idx].name;
}

IconId iconFromName(const char *name) {
    if (!name) return IconId::None;
    for (uint8_t i = 0; i < (uint8_t)IconId::Count; i++) {
        if (strcmp(ICON_TABLE[i].name, name) == 0) return (IconId)i;
    }
    return IconId::None;
}

